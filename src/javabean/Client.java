package javabean;

public class Client 
{
	private int client_id;
	private String identityCard;
	private int clientIdentity;
	private String unitName;
	private String phoneNub;
	private String mobileNub;
	private String address;
	private String postcode;
	private String linkman;
	private String email;
	
	public Client(){
		
	}
	
	public int getClient_id() {
		return client_id;
	}
	public void setClient_id(int client_id) {
		this.client_id = client_id;
	}
	public String getIdentityCard() {
		return identityCard;
	}
	public void setIdentityCard(String identityCard) {
		this.identityCard = identityCard;
	}
	public int getClientIdentity() {
		return clientIdentity;
	}
	public void setClientIdentity(int clientIdentity) {
		this.clientIdentity = clientIdentity;
	}
	public String getUnitName() {
		return unitName;
	}
	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}
	public String getPhoneNub() {
		return phoneNub;
	}
	public void setPhoneNub(String phoneNub) {
		this.phoneNub = phoneNub;
	}
	public String getMobileNub() {
		return mobileNub;
	}
	public void setMobileNub(String mobileNub) {
		this.mobileNub = mobileNub;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPostcode() {
		return postcode;
	}
	public void setPostcode(String postcode) {
		this.postcode = postcode;
	}
	public String getLinkman() {
		return linkman;
	}
	public void setLinkman(String linkman) {
		this.linkman = linkman;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
}
